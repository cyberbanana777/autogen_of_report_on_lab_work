    def parse_sections(self, text):
        """–ü–∞—Ä—Å–∏–Ω–≥ —Ç–µ–∫—Å—Ç–∞ –Ω–∞ —Å–µ–∫—Ü–∏–∏"""
        sections = {}
        lines = text.strip().split('\n')
        current_section = None
        content = []
        
        for line in lines:
            if line.startswith('[SECTION_START'):
                current_section = line.split(' ')[1].replace(']', '')
                content = []
            elif line.startswith('[SECTION_END'):
                if current_section:
                    sections[current_section] = '\n'.join(content).strip()
                current_section = None
                content = []
            elif current_section is not None:
                content.append(line)
        
        return sections
    
    def set_page_settings(self):
        """–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ —Å—Ç—Ä–∞–Ω–∏—Ü—ã"""
        section = self.doc.sections[0]
        section.page_height = Cm(29.7)  # A4
        section.page_width = Cm(21)     # A4
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(3)
        section.right_margin = Cm(1.5)
    
    def setup_page_numbers(self):
        """–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –Ω—É–º–µ—Ä–∞—Ü–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü"""
        for section in self.doc.sections:
            footer = section.footer
            if not footer:
                continue
                
            # –û—á–∏—â–∞–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π footer
            for paragraph in footer.paragraphs:
                p = paragraph._element
                p.getparent().remove(p)
            
            # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π footer —Å –Ω—É–º–µ—Ä–∞—Ü–∏–µ–π
            paragraph = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
            paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
            
            # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª–µ –Ω–æ–º–µ—Ä–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
            run = paragraph.add_run()
            fld_char = OxmlElement('w:fldChar')
            fld_char.set(qn('w:fldCharType'), 'begin')
            run._r.append(fld_char)
            
            instr_text = OxmlElement('w:instrText')
            instr_text.set(qn('xml:space'), 'preserve')
            instr_text.text = 'PAGE'
            run._r.append(instr_text)
            
            fld_char = OxmlElement('w:fldChar')
            fld_char.set(qn('w:fldCharType'), 'end')
            run._r.append(fld_char)
            
            # –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–æ–º–µ—Ä–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
            for run in paragraph.runs:
                run.font.name = 'Times New Roman'
                run.font.size = Pt(10)
    
    def add_heading(self, text, level=1):
        """–î–æ–±–∞–≤–ª–µ–Ω–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞"""
